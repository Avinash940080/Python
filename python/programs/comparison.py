#Comparison operators in python are used to compare two or more variables or values.
#Some of the various comparison operators are

'''
Equal to '==' checks if the value on the left is equal to the value on the right.

Not equal to '!=' checks if the value on the left is not equal to the value on the right.

Greater than '>' checks if the value on the left is greater than the value on the right.

Less than '<' checks if the value on the left is less than the value on the right.

Greater than or equal to '>=' checks if the value on the left is greater or equal to the value on the right.

less than or equal to '<=' checks if the value on the left is less than or equal to the value on the right.

'''

#Example programs

x = int(input('Enter the value of a:'))
y = int(input('Enter the value of b:'))

print(x==y) #Equal to

print(x!=y) #Not equal to

print(x>y) #Greater than

print(x<y) #Less than 

print(x>=y) #Greater than or equal to 

print(x<=y) #less than or equal to