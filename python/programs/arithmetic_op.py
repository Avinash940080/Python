#Arithmetic operators is used with numeric values to perform normal mathematical calculations.
#Arithmetic operators are
'''
Addition (+): Adds two numbers.

Subtraction (-): Subtracts the right operand from the left operand.

Multiplication (*): Multiplies two numbers.

Division (/): Divides the left operand by the right operand.

Modulus (%): Returns the remainder of the division of the left operand by the right operand.

Exponentiation (**): Raises the left operand to the power of the right operand.

Floor Division (//): Divides the left operand by the right operand and returns the largest integer less than or equal to the result.

'''

#examples of the arithmetic operators

print(5+2) #addition
print(5-2) #subtraction
print(5*2) #multiplication
print(5/2) #division
print(5%2) #modulus, it gives remainder of the given operation.
print(5**2) #exponentiation, it gives exponent values.
print(5//2) #floor division, it returns the largest integer less than or equal to the result.


